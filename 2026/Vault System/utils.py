import os
import logging

def get_width():
    try:
        width = os.get_terminal_size().columns
    except OSError:
        width = 80

    return width
# End function

def get_padding(text):
    terminal_width = get_width()

    left_padding = max((terminal_width - text) // 2, 0)

    return left_padding
# End function

def clear_screen():
    if os.name == "nt":
        os.system("cls")
    else:
        os.system("clear")
# End function

